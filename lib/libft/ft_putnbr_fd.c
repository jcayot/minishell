#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	ln;

	ln = n;
	if (ln < 0)
	{
		ft_putchar_fd('-', fd);
		ln *= -1;
	}
	if (ln > 9)
		ft_putnbr_fd((int)(ln / 10), fd);
	ft_putchar_fd((int)((ln % 10) + '0'), fd);
}
