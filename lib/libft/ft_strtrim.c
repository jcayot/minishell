#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	size_t	i;

	i = 0;
	if (s1 == NULL || set == NULL)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (s1 < &s1[i - 1] && ft_strchr(set, s1[i - 1]))
		i--;
	str = ft_substr(s1, 0, i);
	return (str);
}
